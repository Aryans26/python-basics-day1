# Basic Python: Variables, Data Types, and print()

# Variables hold values
name = "Suhaib"          # string
age = 30                 # integer
height_m = 1.72          # float
is_student = False        # boolean

# print() displays output on the screen
print("Name:", name)
print("Age:", age)
print("Height (m):", height_m)
print("Student?", is_student)

# You can change variable values
age = age + 1
print("Next year, age:", age)

# type() tells you the data type
print(type(name))        # Shows: <class 'str'>
print(type(age))         # Shows: <class 'int'>
print(type(height_m))    # Shows: <class 'float'>
print(type(is_student))  # Shows: <class 'bool'>
